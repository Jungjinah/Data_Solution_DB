CREATE TABLE BOOKING (
    B_NO NUMBER(3) PRIMARY KEY,
    B_NAME VARCHAR2(10 CHAR) NOT NULL,
    B_DATE DATE NOT NULL,
    B_PHONENUM VARCHAR2(20 CHAR) NOT NULL,
    B_LOCATION VARCHAR2(10 CHAR) NOT NULL
);

CREATE SEQUENCE BOOKING_SEQ;
INSERT INTO BOOKING VALUES (BOOKING_SEQ.NEXTVAL, '김길동', TO_DATE('2022-01-13 18:00', 'YYYY-MM-DD HH24:MI'), '010-1111-2222', '강남');
INSERT INTO BOOKING VALUES (BOOKING_SEQ.NEXTVAL, '이길동', TO_DATE('2022-02-13 12:00', 'YYYY-MM-DD HH24:MI'), '010-2222-3333', '강남');
