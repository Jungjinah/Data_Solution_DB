DROP TABLE OWNERS;
DROP TABLE BOOKING;
DROP SEQUENCE BOOKING_SEQ;

CREATE TABLE OWNERS (
    O_LOCATION VARCHAR2(10 CHAR) PRIMARY KEY,
    O_NAME VARCHAR2(10 CHAR) NOT NULL,
    O_SEAT NUMBER(3) NOT NULL
);

