CREATE TABLE MARKET(
    M_NO NUMBER(4) PRIMARY KEY,
    M_NAME VARCHAR2(10 CHAR) NOT NULL,
    M_LOCATION VARCHAR2(10 CHAR) NOT NULL,
    M_LAND NUMBER(4) NOT NULL,      -- 그 매장의 평수
    M_PARKING NUMBER(4) NOT NULL    -- 주차 가능한 대수
);

