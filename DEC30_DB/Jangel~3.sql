    SET P_PRICE = P_PRICE * 0.9
        WHERE P_M_NO IN (
            SELECT M_NO
            FROM MARKET
            WHERE M_NAME = '홈플러스' AND M_LOCATION = '부천'
        );

-- 제일 비싼거 10%할인
UPDATE PRODUCT
    SET P_PRICE = P_PRICE * 0.9
        WHERE  P_PRICE = (
        SELECT MAX(P_PRICE)
        FROM PRODUCT
    );
